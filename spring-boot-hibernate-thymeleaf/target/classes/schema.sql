DROP TABLE IF EXISTS TBL_EMPLOYEES;
 
CREATE TABLE TBL_EMPLOYEES (
  id INT AUTO_INCREMENT  PRIMARY KEY,
  first_name VARCHAR(250) NOT NULL,
  last_name VARCHAR(250) NOT NULL,
  employment_id VARCHAR(250) NOT NULL,
  start_date VARCHAR(250) NOT NULL,
  end_date VARCHAR(250) NOT NULL,
  designation VARCHAR(250) NOT NULL,
  department VARCHAR(250) NOT NULL,
  status VARCHAR(250) NOT NULL,
  DOB VARCHAR(250) NOT NULL,
  reporting_mgr VARCHAR(250) NOT NULL,
  gender CHAR(25) NOT NULL,
  blood_group VARCHAR(250) NOT NULL,
  address VARCHAR(250) NOT NULL
);

DROP TABLE IF EXISTS TBL_DEPENDANTS;

CREATE TABLE TBL_DEPENDANTS (
  id INT AUTO_INCREMENT  PRIMARY KEY,
  employee_id int(10) NOT NULL,
  first_name VARCHAR(250) NOT NULL,
  last_name VARCHAR(250) NOT NULL,
  gender CHAR(25) NOT NULL,
  DOB VARCHAR(250) NOT NULL,
  relationship VARCHAR(250) NOT NULL,
  FOREIGN KEY (employee_id) REFERENCES TBL_EMPLOYEES(id)
);

DROP TABLE IF EXISTS TBL_EDUCATIONAL_QUALIFICATIONS;

CREATE TABLE TBL_EDUCATIONAL_QUALIFICATIONS (
  id INT AUTO_INCREMENT PRIMARY KEY,
  employee_id int(10) NOT NULL,
  type VARCHAR(250) NOT NULL,
  start_date VARCHAR(250) NOT NULL,
  end_date VARCHAR(250) NOT NULL,
  institution VARCHAR(250) NOT NULL,
  address VARCHAR(250) NOT NULL,
  percentage FLOAT(10) NOT NULL,
  FOREIGN KEY (employee_id) REFERENCES TBL_EMPLOYEES(id)
);